'      const flashR = headR * (0.2 + (1 - k) * 1.0);',
'      const flashAlpha = k * 0.9;',
'      ctx.fillStyle = k > 0.65 ? \'rgba(255,240,100,\' + flashAlpha + \')\' : \'rgba(255,150,40,\' + flashAlpha + \')\';',
'      ctx.beginPath(); ctx.arc(barrelLen + headR * 0.2, 0, flashR, 0, Math.PI * 2); ctx.fill();',
'    }',
'    ctx.restore();',
'  }',
'',
'  // ═══════════ 法杖 (control 兼容) ═══════════',
'  else if (weapon === \'staff\') {',
'    ctx.save(); ctx.translate(rx, ry); ctx.scale(dir, 1); ctx.rotate(0.5 - k * 0.25);',
