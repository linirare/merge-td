'    // 炮身主体',
'    ctx.beginPath();',
'    ctx.moveTo(-headR * 0.3, -barrelW);',
'    ctx.lineTo(barrelLen, -barrelW);',
'    ctx.arc(barrelLen, 0, barrelW, -Math.PI / 2, Math.PI / 2);',
'    ctx.lineTo(-headR * 0.3, barrelW);',
'    ctx.closePath();',
'    ctx.fillStyle = \'rgba(80,65,55,0.9)\'; ctx.fill();',
'    ctx.strokeStyle = ink; ctx.lineWidth = headR * 0.18; ctx.stroke();',
'    // 炮口环(加粗)',
'    ctx.beginPath(); ctx.arc(barrelLen, 0, barrelW * 1.2, 0, Math.PI * 2);',
'    ctx.strokeStyle = ink; ctx.lineWidth = headR * 0.16; ctx.stroke();',
